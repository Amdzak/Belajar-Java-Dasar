public class Main {
	
	/*
		Pengenalan variabel variabel merupakan sebuah penamaan untuk suatu tipe data atau apapun 
		variabel biasanya di buat deklarasi atau assingment 
		contoh : 
		int a;     a merupakan variabel yang memiliki tipe data integer 
		
		int awal;    ini merupakan variabel deklarasi karena tidak memiliki nilai di dalamnya 
		int akhir = 10;  ini merupakan variabel assingment karena memiliki nilai di dalamnya
	*/
	
	public static void main (String[] args) {
		// Tipe variabel
		double awal = 10;     // Assingment 
		int akhir;     // deklarasi 
		
		akhir = 12;
		
		System.out.println(awal);
		System.out.println(akhir);
		}
}